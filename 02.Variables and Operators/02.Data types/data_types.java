// primitive data type
class DataType {
    public static void main (String a[]) {
        int num1 = 9; // 4 byte - 32 bits
        byte by = 127; // 1 byte - 8 bits
        short sh = 558; // 2 byte - 16 bits
        long l = 554433l; // 8 bytes - 64 bits

        float f = 5.4f; // 4 bytes - 32 bits
        double d = 5.4; // default - 8 bytes - 64 bits

        char c = 'k'; // 2 bytes - 16 bits

        boolean b = true;
        boolean abc = false;
    }
}